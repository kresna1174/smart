                   <p align=justify>
                   Yayasan  Mardi  Putera  Tulungagung mempunyai maksud dan tujuan antara lain “membuka sekolahan-sekolahan yang sesuai dengan kebutuhan masyarakat untuk kemudian jika diperlukan diserahkan kepada Pemerintah“.<br>

Pada  tahun  1957 / 1958  berdirilah  SMA  Mardi  Putera  yang  kemudian  dinegerikan  pada  tahun 1960 /1961. Pada tahun pelajaran 1960 / 1961 didirikanlah SMEA Mardi Putera yang dipimpin oleh Bapak Mas Marni Adiwijata (alm.) sebagai Kepala Sekolah dan Bapak Soediharto, BA. (alm.) sebagai Wakil Kepala Sekolah dengan staf pengajar yang terdiri dari guru-guru Yayasan dan dari SMA Negeri Tulungagung.<br>

SMEA Mardi Putera Tulungagung pada mulanya menempati gedung SMP Negeri 1  Tulungagung dan pada tahun 1965 pindah ke gedung yang sekarang berada di Jalan Panglima Jendral Sudirman VI / 2 Tulungagung. Berdasarkan Surat Keputusan Mendikbud R.I No. 0124/O/1972 tanggal 10 Agustus 1872 yang berlaku per tanggal 02 Januari 1972, SMEA Mardi Putera berubah menjadi SMEA Negeri Tulungagung dan sekarang menjadi UPTD SMK Negeri 1 Boyolangu Tulungagung serta pindah dari Desa Kepatihan ke Desa Beji, Jalan Ki Mangunsarkoro VI / 3 Boyolangu, Tulungagung sejak tahun 1996. <br>


Tahun 2017 seluruh SLTA/SMK menjadi bagian dari Dinas Pendidikan Provinsi Jawa Timur. Mulai  saai ini UPTD SMK Negeri 1 Boyolangu Tulungagung berubah menjadi SMK Negeri 1 Boyolangu Tulungagung.
</p>
